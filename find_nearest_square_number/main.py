from math import sqrt

def nearest_sq(n):
    up = 0
    down = 0
    for i in range(n, n+100000):
        if int(sqrt(i)) == sqrt(i):
            up = i 
            break
    for i in range(n, 0, -1):
        if int(sqrt(i)) == sqrt(i):
            down = i
            break
    if abs(up - n) > abs(down - n):
        return down
    else:
        return up

